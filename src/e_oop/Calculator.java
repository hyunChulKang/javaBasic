package e_oop;

public class Calculator {

	double thehagi (double a, double b){
		return a + b;
	}
	
	double bbegi(double a, double b){
		return a - b;
	}
	
	double gopagi(double a, double b){
		return a * b;
	}
	
	double nanugi (double a, double b){
		return a / b;
	}
	
	double nameji (double a, double b){
		return a % b;
	}
	
}
